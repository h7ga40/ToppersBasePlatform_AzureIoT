/*
 *  フォントの幅を取得する
 *  param1  phead: フォントヘッダ
 *  param2  c: キャラクタ
 *  param3  len:キャラクタの長さ領域へのポインタ
 *  return  フォント幅
 */
static int
GetFontWidth(font_head_t *phead, uint8_t c, int *len)
{
	const font_t  **pf = phead->font_table;
	int   width;

	width = phead->font_width;
	*len = 1;
	if(phead->file_attribute == 0){
		if(c < 0x80 || c >= 0xA0)
			width = pf[c]->width;
	}
	else if((c & 0xE0) == 0xC0)
		*len = 2;
	else if((c & 0xF0) == 0xE0)
		*len = 3;
	else if((c & 0xF8) == 0xF0)
		*len = 4;
	else if(c < 0x80 || c >= 0xA0)
		width = pf[c]->width;
	return width;
}

