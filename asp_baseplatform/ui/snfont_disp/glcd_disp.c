/*
 *  文字列描画
 *  param1  pDrawProp: Pointer to Draw Prop
 *  param1  x0: 文字列描画 X座標始点
 *  param2  y0: 文字列描画 Y座標始点
 *  param3  Text: 描画文字列へのポインタ
 *  param4  Mode: 表示モード
 */
void
lcd_DisplayStringAt(LCD_DrawProp_t *pDrawProp, uint16_t x0, uint16_t y0, uint8_t *Text, Line_ModeTypdef Mode)
{
	LCD_Handler_t *hlcd = pDrawProp->hlcd;
	font_head_t   *phead = (font_head_t *)pDrawProp->pFont;
	const font_t  **pf = phead->font_table;
	uint16_t ref_column = 1;
	uint32_t size = 0;
	uint8_t  *ptr = (uint8_t *)Text, c;
	int      wid, len  = 1;

	/* Get the text size */
	while(*ptr){
		size += GetFontWidth(phead, *ptr, &len);
		ptr  += len;
	}

	switch (Mode){
	case CENTER_MODE:
		ref_column = x0 + (hlcd->_width - size) / 2;
		break;
	case LEFT_MODE:
		ref_column = x0;
		break;
	case RIGHT_MODE:
		ref_column = - x0 + (hlcd->_width - size);
		break;
	default:
		ref_column = x0;
		break;
	}

	/*
	 *  開始カラム位置をスクリーン内に収める
	 */
	if((ref_column < 1) || (ref_column >= 0x8000)){
		ref_column = 1;
	}

	/*
	 *  一文字描画を使って文字列を描画する
	 */
	while (((c = *Text) != 0) && pf[c] != 0 && (ref_column + (wid = GetFontWidth(phead, c, &len))) <= hlcd->_width){
		lcd_DisplayData(pDrawProp, ref_column, y0, Text);
		ref_column += wid;	/* 次のカラムに移動 */
		Text += len;		/* 次の文字に移動 */
	}
}

/*
 *  文字列描画
 *  param1  pDrawProp: Pointer to Draw Prop
 *  param2  x0: 文字列描画 X座標始点
 *  param3  y0: 文字列描画 Y座標始点
 *  param4  Text: 描画文字列へのポインタ
 */
void
lcd_DisplayStringAtLine(LCD_DrawProp_t *pDrawProp, uint16_t Line, uint8_t *ptr)
{
	font_head_t  *phead = (font_head_t *)pDrawProp->pFont;
	const font_t *pf;

	pf = phead->font_table[' '];
	lcd_DisplayStringAt(pDrawProp, 0, Line * pf->height, ptr, LEFT_MODE);
}

