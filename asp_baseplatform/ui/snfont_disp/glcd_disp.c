/*
 *  2バイトコードビットマップ番号
 */
static int
GetFont2BNo(font_work_t *pft, uint8_t *ptr)
{
	int      totalk = 0;
	uint32_t i, j;

	for(i = 0 ; i < UTF8_CODE_2B_1_NUM; i++){
		if(Utf8CodeTable_2B_1st[i][0] == ptr[0]) {
			for(j = 0 ; j < Utf8CodeTable_2B_1st[i][1] ; j++) {
				if(Utf8CodeTable_2B_2nd[totalk] == ptr[1])
					return totalk;
				totalk++;
			}
		}
		else
			totalk += Utf8CodeTable_2B_1st[i][1];
	}
	return -1;
}

