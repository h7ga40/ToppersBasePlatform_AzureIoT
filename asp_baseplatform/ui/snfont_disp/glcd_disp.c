/*
 *  フォントデータからフォントテーブルを取り出す
 *  param1  phead: フォントヘッダ
 *  param2  ptr: フォントデータへのポインタ
 *  return  フォントテーブルへのポインタ
 */
const font_t *
lcd_getFontTable(font_head_t *phead, uint8_t *ptr)
{
	const font_t **pf = phead->font_table;
	int len;
#ifndef NOT_USEFILFONT
	char  fname[32+4];
	font_t *pwf = (font_t *)font_data;
	font_file_head_t *pffh;
	font_work_t  *pft = NULL;
	int no, i, k, off = 0;
#endif

	GetFontWidth(phead, *ptr, &len);
	if(len == 1)
		return pf[*ptr];
	else if(phead->file_attribute == 0)
		return pf[*ptr];
	else{
#ifndef NOT_USEFILFONT
		pwf->width   = phead->font_width;
		pwf->height  = phead->font_height;
		pwf->stride  = (phead->font_width + 7) >> 3;
		pwf->iheight = phead->font_height;
		memset(pwf->image, 0, pwf->height * pwf->stride);

		if(phead->pdata == NULL){
			for(i = 0 ; i < NUM_FONT_WORK ; i++){
				if(FontWork[i].pffont == NULL){
					pft = &FontWork[i];
					break;
				}
			}
			if(pft != NULL){
				sprintf(fname, "%s.fnt", phead->font_name);
				pft->pffont = fopen(fname, "rb");
				if(pft->pffont != NULL){
					if(fread(pft->table, sizeof(font_file_head_t), 1, pft->pffont) != 1){
						phead->file_attribute = 0x00;
						fclose(pft->pffont);
						pft->pffont = NULL;
					}
					else{
						pffh = (font_file_head_t *)pft->table;
						pft->offset_image_2B = pffh->off_2byte_font;
						pft->offset_image_3B = pffh->off_3byte_font;
						pft->num_table2B = pffh->siz_2byte_font;
						pft->num_table3B = pffh->siz_3byte_font;
						pft->image_size  = pffh->csize + pffh->isize;
						phead->pdata = pft;
					}
				}
				else
					phead->file_attribute = 0x00;
			}
			else
				phead->file_attribute = 0x00;
		}
		if((pft = phead->pdata) != NULL){
			no = -1;
			if(len == 2){
				no = GetFont2BNo(pft, ptr);
				off = pft->offset_image_2B;
			}
			else if(len == 3){
				no = GetFont3BNo(pft, ptr);
				off = pft->offset_image_3B;
			}
			if(no >= 0){
				uint32_t length = 0;
				uint32_t data = 0, j, c;
				fseek(pft->pffont, (no * pft->image_size + off), SEEK_SET);
				fread(pft->table, pft->image_size, 1, pft->pffont);

				for(i = j = k = no = 0 ; i < phead->font_height ; i++){
					while(length < phead->font_width){
						data <<= 8;
						data |= pft->table[no+1];
						length += 8;
						no++;
					}
					j = phead->font_width;
					while(j >= 8){
						c = data >> (length - 8);
						j -= 8;
						length -= 8;
						pwf->image[k++] = c;
					}
					if(j > 0){
						c = data << (8-j);
						length -= j;
						pwf->image[k++] = c;
					}
				}
			}
		}
		return pwf;
#else
		return pf[*ptr];
#endif
	}
}

